//ASSIGNMENT OPERATORS

//assignment operator
let x = 20;

//addition assignment operator
x += 1; //x = x + 1
console.log(x);

//subtraction assignment operator
x -= 1; //x = x - 1
console.log(x);

//multiplication assignment operator
x *= 2; // x = x * 2
console.log(x);

//division assignment operator
x /= 4; //x = x / 4
console.log(x);

//remainder/modulus assignment operator
x %= 2; //x = x / 2 (ingore the whole number, take the remainder) (aka x = x % 2)
console.log(x);

//exponential assignment operator
x **= 2; // x = x^2
console.log(x);